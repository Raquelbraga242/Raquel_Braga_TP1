def ordenar_cartas(cartas):
    n = len(cartas)

    for i in range(1, n):

        carta_atual = cartas[i]
        j = i - 1

        while j >= 0 and cartas[j] > carta_atual:
            cartas[j + 1] = cartas[j]
            j -= 1

        cartas[j + 1] = carta_atual

    return cartas


cartas = [7, 3, 10, 1, 13, 5, 2, 12, 8, 6, 11, 4, 9]
print("Antes:", cartas)
ordenado = ordenar_cartas(cartas)
print("Depois:", ordenado)

