def greatestNumber(array):
    if not array:
        return None

    maior = array[0]

    i = 1
    while i < len(array):

        if array[i] > maior:
            maior = array[i]
        i += 1

    return maior


array = [3, 5, 2, 9, 4]
print(greatestNumber(array))

