def bubble_sort_left_right(lista):
    n = len(lista)

    left = 0
    right = n - 1

    while left < right:

        for j in range(left, right):
            if lista[j] > lista[j + 1]:
                lista[j], lista[j + 1] = lista[j + 1], lista[j]

        right -= 1

        for j in range(right, left, -1):
            if lista[j] < lista[j - 1]:
                lista[j], lista[j - 1] = lista[j - 1], lista[j]

        left += 1
lista = [64, 34, 33, 12, 14, 13, 65, 38, 2]

bubble_sort_left_right(lista)
print(lista)

